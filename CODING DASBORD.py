        "std_soal = soal.std()\n",
        "\n",
        "analisis_soal = pd.DataFrame({\n",
        "    \"Rata-rata\": rata_soal,\n",
        "    \"Standar Deviasi\": std_soal\n",
        "})\n",
        "\n",
        "st.dataframe(analisis_soal)\n",
        "\n",
        "fig2 = plt.figure()\n",
        "rata_soal.plot(kind=\"bar\")\n",
        "plt.xlabel(\"Soal\")\n",
        "plt.ylabel(\"Rata-rata Skor\")\n",
        "st.pyplot(fig2)\n",
        "\n",
        "# =========================\n",
        "# 3. SOAL TERBAIK & TERBURUK\n",
        "# =========================\n",
        "st.header(\"3️⃣ Soal Terbaik & Terburuk\")\n",
        "\n",
        "soal_terbaik = rata_soal.idxmax()\n",
        "soal_terburuk = rata_soal.idxmin()\n",
        "\n",
        "st.success(f\"Soal Termudah: {soal_terbaik}\")\n",
        "st.error(f\"Soal Tersulit: {soal_terburuk}\")\n",
