        "col2.metric(\"Rata-rata Total Skor\", round(df[\"Total_Skor\"].mean(),2))\n",
        "col3.metric(\"Standar Deviasi\", round(df[\"Total_Skor\"].std(),2))\n",
        "\n",
        "fig1 = plt.figure()\n",
        "plt.hist(df[\"Total_Skor\"], bins=10)\n",
        "plt.xlabel(\"Total Skor\")\n",
